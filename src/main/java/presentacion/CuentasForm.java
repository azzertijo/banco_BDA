    public void cargarTablaCuentas(){
        try{
            List<Cuenta> listaCuentas = this.cuentasDAO.consultar(this.configPaginado,this.cliente);
            DefaultTableModel modeloTabla = (DefaultTableModel)this.tblCuentas.getModel();
            modeloTabla.setRowCount(0);
            for(Cuenta cuenta: listaCuentas){
                Object[] fila = {cuenta.getNum_cuenta(),cuenta.getSaldo(),cuenta.getFecha_apertura()};
            modeloTabla.addRow(fila);
            }
        }catch(PersistenciaException ex){

        }
    }
    
